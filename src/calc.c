#include "../include/fdf.h"

float	mod(float i)
{
	if (i < 0)
		i = -i;
	return (i);
}

void	isometric(float *x, float *y, int z)
{
	*x = (*x - *y) * cos(0.8);
	*y = (*x + *y) * sin(0.8) - z;
}
